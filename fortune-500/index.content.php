<p>
    The Fortune 500 is an annual list compiled and published by Fortune magazine that ranks 500 of 
    the largest United States corporations by total revenue for their respective fiscal years. 
    The list includes publicly held companies, along with privately held companies for which revenues are publicly available.
</p>
<p>The annual list of 500 companies represents the bedrock of American business and remains 
    an important tool for both researchers and investors.
</p>
<p>On this website, I would show the Fortune 500 data for year of 2021 with following features.</p>
    <ul>
        <li>Company - the name of the company</li>
        <li>Rank - The 2021 rank established by Fortune (1-500)</li>
        <li>Revenue - Revenue of each company in millions. This is the criteria used to rank each company.</li>
        <li>Profit - Profit of each company in millions.</li>
        <li>Sector - The sector of the market the company operates in.</li>
        <li>Num Employee - The number of employees</li>
        <li>Newcomer - Indicates whether or not the company is new to the top Fortune 500 (1 for "yes" or 0 for "no"). No value will be listed for companies outside of the top 500.</li>
        <li>CEO Founder - Indicates whether the CEO of the company is also the founder (1 for "yes" or 0 for "no").</li>
        <li>CEO Woman - Indicates whether the CEO of the company is a woman (1 for "yes" or 0 for "no").</li>
    </ul>
    
<p>With given features, I was able to find following: </p>
<ul>
    <li>Walmart is the largest company by revenue and almost 2 time larger than second/ third largest</li>
    <li>Retailing is the sector with the most companies on the Fortune 500</li>
    <li>3% of companies have CEOs who were also the founder of companies</li>
    <li>8% of companies have CEOs who were woman.</li>
</ul>